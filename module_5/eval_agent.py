"""
Module 5 — Evaluation Agent (Part C)
用 Hannia 的 Quality Auditor system prompt 逐条评估 run_log.json 中的 outreach 输出。

每条输出评估 3 个维度 (1-5):
  1. Groundedness — 是否基于真实的 client memory + trend data
  2. Over-Promotion Guardrail — 是否过度推销
  3. "Would You Send" — CA 能不能直接发出去

用法:
  python3 module_5/eval_agent.py                  # 评估全部 run_log
  python3 module_5/eval_agent.py --limit 5        # 只评前 5 条
"""
from __future__ import annotations

import argparse
import json
import os
import re
import sys
from datetime import datetime, timezone
from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parent.parent
if str(_REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(_REPO_ROOT))


def _load_env_file(path: Path) -> None:
    if not path.exists():
        return
    for line in path.read_text().splitlines():
        line = line.strip()
        if "=" in line and not line.startswith("#"):
            k, v = line.split("=", 1)
            os.environ.setdefault(k.strip(), v.strip())


_load_env_file(_REPO_ROOT / ".env")

EVAL_SYSTEM_PROMPT = """Role: You are a Senior Boutique Manager at LVMH. Your job is to strictly audit outreach messages generated by an AI agent to ensure they meet the Maison's high standards for clienteling.

Evaluation Dimensions:

1. Groundedness (1–5)
   - Definition: Is the message supported by the Client Memory and Trend ID?
   - Score 1: Mentions facts not found in the data (hallucinations) or has no connection to real data.
   - Score 3: Some connection to the data, but uses vague or generic insights.
   - Score 5: Every detail is directly traceable to specific data fields or quotes in the input.

2. Over-Promotion Guardrail (1–5)
   - Definition: Does the message respect the brand tone or is it too "salesy"?
   - Score 1: Aggressive, "AI magic" tone that pushes a sale and ignores the human relationship.
   - Score 3: Feels like a template; contains slight exaggerations of product benefits.
   - Score 5: Sophisticated, relationship-first tone; the product feels like a natural, subtle suggestion.

3. "Would You Send" Score (1–5)
   - Definition: The overall usability and professional quality of the output.
   - Score 1: Unusable, unnatural, or contains glaring bot-like errors.
   - Score 3: Decent effort but requires manual edits before a Client Advisor (CA) could send it.
   - Score 5: Ready to send as-is; high-quality personalization that a real CA would be proud of.

Required Output Format (strict JSON):
{
  "groundedness": {"score": <int 1-5>, "justification": "<1-2 sentences>"},
  "over_promotion": {"score": <int 1-5>, "justification": "<1-2 sentences>"},
  "would_you_send": {"score": <int 1-5>, "justification": "<1-2 sentences>"},
  "key_phrases": ["<phrase1>", "<phrase2>", ...],
  "top_failure": "<If any score is <3, explain the specific failure case. Otherwise 'None'>"
}

You MUST output ONLY the JSON object above — no markdown, no extra text."""

M5_DIR = Path(__file__).resolve().parent
RUN_LOG_PATH = M5_DIR / "run_log.json"
CLIENTS_PATH = M5_DIR / "benchmark" / "benchmark_clients.json"
TRENDS_PATH = M5_DIR / "benchmark" / "benchmark_trend_shortlist.json"
EVAL_OUTPUT_PATH = M5_DIR / "eval_results.json"


def _call_llm(system_prompt: str, user_prompt: str) -> str:
    """Reuse agent.py's LLM routing logic."""
    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    model = os.environ.get("DEFAULT_MODEL", "claude-sonnet-4-20250514")
    base_url = os.environ.get("ANTHROPIC_API_BASE_URL", "").strip()

    if "openrouter" in base_url.lower():
        from openai import OpenAI
        key = os.environ.get("OPENROUTER_API_KEY", "").strip() or api_key
        client = OpenAI(
            base_url=base_url.rstrip("/"),
            api_key=key,
            default_headers={
                "HTTP-Referer": "https://github.com/m-ny-mvp",
                "X-Title": "Module 5 Eval Agent",
            },
        )
        resp = client.chat.completions.create(
            model=model, max_tokens=1024, temperature=0.2,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
        )
        return (resp.choices[0].message.content or "").strip()
    else:
        import anthropic
        client = anthropic.Anthropic(api_key=api_key, **({"base_url": base_url} if base_url else {}))
        resp = client.messages.create(
            model=model, max_tokens=1024, temperature=0.2,
            system=system_prompt,
            messages=[{"role": "user", "content": user_prompt}],
        )
        return resp.content[0].text.strip()


def _parse_eval(raw: str) -> dict | None:
    text = raw.strip()
    if "```json" in text:
        text = text.split("```json")[1].split("```")[0].strip()
    elif "```" in text:
        text = text.split("```")[1].split("```")[0].strip()
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        return None


def _build_user_prompt(client: dict, trends: list[dict], parsed_output: dict) -> str:
    drafts_text = ""
    for i, d in enumerate(parsed_output.get("wechat_drafts", []), 1):
        drafts_text += f"\n  Draft {i} [{d.get('tone', '')}]: {d.get('message', '')}"

    evidence_text = "\n".join(f"  - {e}" for e in parsed_output.get("evidence_used", []))

    trend_ids_used = [e for e in parsed_output.get("evidence_used", []) if "trend_id" in e.lower()]
    trend_context = ""
    for t in trends:
        tid = t.get("trend_id", "")
        if any(tid in ref for ref in parsed_output.get("evidence_used", [])):
            trend_context += f"\n  - {tid}: {t.get('trend_label', '')} — {t.get('cluster_summary', '')}"

    return f"""Please evaluate the following AI-generated outreach output.

## Client Memory (Ground Truth)
- client_id: {client.get("client_id", "?")}
- name: {client.get("name", "?")}
- raw_voice_note: {client.get("raw_voice_note", "N/A")}
- summary: {client.get("summary", "N/A")}
- life_event: {json.dumps(client.get("life_event", {}), ensure_ascii=False)}
- timeline: {json.dumps(client.get("timeline", {}), ensure_ascii=False)}
- aesthetic_preference: {json.dumps(client.get("aesthetic_preference", {}), ensure_ascii=False)}
- mood: {json.dumps(client.get("mood", {}), ensure_ascii=False)}
- budget: {json.dumps(client.get("budget", {}), ensure_ascii=False)}
- next_step_intent: {json.dumps(client.get("next_step_intent", {}), ensure_ascii=False)}
- trend_signals: {json.dumps(client.get("trend_signals", {}), ensure_ascii=False)}

## Trend IDs Referenced
{trend_context or "  (none)"}

## AI-Generated Output
- Best Angle: {parsed_output.get("best_angle", "N/A")}
- Angle Summary: {parsed_output.get("angle_summary", "N/A")}
- Evidence Used:
{evidence_text}
- WeChat Drafts:{drafts_text}
- Confidence: {parsed_output.get("confidence", "N/A")}
- Next Step: {parsed_output.get("next_step", "N/A")}

Score each dimension 1-5 and output the JSON."""


def main():
    parser = argparse.ArgumentParser(description="Module 5 — Evaluation Agent")
    parser.add_argument("--limit", type=int, default=0, help="Only evaluate first N entries")
    args = parser.parse_args()

    with open(RUN_LOG_PATH, "r", encoding="utf-8") as f:
        run_logs = json.load(f)

    with open(CLIENTS_PATH, "r", encoding="utf-8") as f:
        clients_data = json.load(f)
    clients_by_id = {c["client_id"]: c for c in clients_data["clients"]}

    with open(TRENDS_PATH, "r", encoding="utf-8") as f:
        trends_data = json.load(f)
    trends = trends_data.get("trends", [])

    entries = run_logs[:args.limit] if args.limit > 0 else run_logs
    print(f"\n🔍 Module 5 — Evaluation Agent")
    print(f"   Evaluating {len(entries)} outreach outputs...\n")

    results = []
    for i, log in enumerate(entries):
        inp = log.get("input", {})
        cid = inp.get("client_id", "")
        name = inp.get("client_name", "")
        parsed = (log.get("output") or {}).get("parsed")

        if not parsed:
            print(f"  [{i+1}/{len(entries)}] {cid} {name} — SKIP (no parsed output)")
            results.append({"client_id": cid, "client_name": name, "skipped": True})
            continue

        client = clients_by_id.get(cid, {"client_id": cid, "name": name})
        user_prompt = _build_user_prompt(client, trends, parsed)

        print(f"  [{i+1}/{len(entries)}] {cid} {name} ...", end=" ", flush=True)
        try:
            raw = _call_llm(EVAL_SYSTEM_PROMPT, user_prompt)
            eval_result = _parse_eval(raw)
        except Exception as e:
            print(f"ERROR: {e}")
            results.append({"client_id": cid, "client_name": name, "error": str(e)})
            continue

        if not eval_result:
            print("PARSE FAIL")
            results.append({"client_id": cid, "client_name": name, "raw": raw, "parse_failed": True})
            continue

        g = eval_result.get("groundedness", {}).get("score", "?")
        o = eval_result.get("over_promotion", {}).get("score", "?")
        w = eval_result.get("would_you_send", {}).get("score", "?")
        print(f"G={g} O={o} W={w}")

        results.append({
            "client_id": cid,
            "client_name": name,
            "scores": {
                "groundedness": eval_result.get("groundedness", {}),
                "over_promotion": eval_result.get("over_promotion", {}),
                "would_you_send": eval_result.get("would_you_send", {}),
            },
            "key_phrases": eval_result.get("key_phrases", []),
            "top_failure": eval_result.get("top_failure", "None"),
        })

    eval_output = {
        "eval_run_at": datetime.now(timezone.utc).isoformat(),
        "total_entries": len(entries),
        "results": results,
    }

    with open(EVAL_OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(eval_output, f, ensure_ascii=False, indent=2)
    print(f"\n✅ Evaluation complete. Results saved to {EVAL_OUTPUT_PATH}")

    # Print summary
    scored = [r for r in results if "scores" in r]
    if scored:
        g_avg = sum(r["scores"]["groundedness"].get("score", 0) for r in scored) / len(scored)
        o_avg = sum(r["scores"]["over_promotion"].get("score", 0) for r in scored) / len(scored)
        w_avg = sum(r["scores"]["would_you_send"].get("score", 0) for r in scored) / len(scored)
        print(f"\n📊 Average Scores ({len(scored)} entries):")
        print(f"   Groundedness:    {g_avg:.2f} / 5")
        print(f"   Over-Promotion:  {o_avg:.2f} / 5")
        print(f"   Would You Send:  {w_avg:.2f} / 5")

        failures = [r for r in scored if r.get("top_failure") and r["top_failure"] != "None"]
        if failures:
            print(f"\n⚠️  {len(failures)} entries with failure cases.")


if __name__ == "__main__":
    main()
